# Operadores aritméticos e lógicos

## Operadores aritméticos
## Adição
soma = 10 + 5
print(soma)

## Subtração
subtracao = 10 - 5
print(subtracao)

## Multiplicação
multiplicacao = 10 * 5
print(multiplicacao)

## Divisão
divisao = 10 / 5
print(divisao)

## Divisão inteira
divisao_inteira = 10 // 5
print(divisao_inteira)

## Resto da divisão
resto = 10 % 5
print(resto)

## Potência
potencia = 10 ** 2
print(potencia)

## Operadores lógicos
## Igualdade
igual = 10 == 5
print(igual)

## Diferença
diferente = 10 != 5
print(diferente)

## Negação
negacao = not (10 == 10)
print(negacao)

## Maior que
maior = 10 > 5
print(maior)

## Menor que
menor = 10 < 5
print(menor)

## Maior ou igual a
maior_ou_igual = 10 >= 5
print(maior_ou_igual)

## Menor ou igual a
menor_ou_igual = 10 <= 5
print(menor_ou_igual)