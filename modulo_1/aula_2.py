# Strings - Textos, sequências de caracteres
### Aspas simples
nome_aspas_simples = 'Formação Engenheiro de IA'

### Aspas duplas
nome_aspas_duplas = "Formação Engenheiro de IA"

### Aspas triplas
nome_aspas_triplas = """Formação Engenheiro de IA"""

### Aspas triplas para múltiplas linhas
