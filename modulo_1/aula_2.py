# Inteiros - Números inteiros
salario_inteiro = 20_000

# Float - Números decimais
salario_float = 20000.90

# Boolean - Verdadeiro ou falso
verificado = True

# Conversão de tipos
## String para inteiro
salario_string = "20000"
salario_inteiro = int(salario_string)

## Float para string
altura_float = 1.75
altura_string = str(altura_float)

## Float para inteiro
altura_float = 1.75
altura_inteiro = int(altura_float)

## Booleano para string
verificado_booleano = True
verificado_string = str(verificado_booleano)

## Strings para booleano
verificado_string = "True"
verificado_booleano = bool(verificado_string)

## Floats para booleano
altura_float = 1.75
altura_booleano = bool(altura_float)